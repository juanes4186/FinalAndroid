public class Casa {

    private String barrio;
    private int codigo_dane_departamento;
    private int codigo_dane_municipio;
    private String departamento;
    private String direccion;
    private String email;
    private String latitud;
    private String longitud;
    private String municipio;
    private String nombre;
    private String telefono_celular;
    private String tipo;


    public String getBarrio() {
        return barrio;
    }

    public void setBarrio(String barrio) {
        this.barrio = barrio;
    }

    public int getCodigo_dane_departamento() {
        return codigo_dane_departamento;
    }

    public void setCodigo_dane_departamento(int codigo_dane_departamento) {
        this.codigo_dane_departamento = codigo_dane_departamento;
    }

    public int getCodigo_dane_municipio() {
        return codigo_dane_municipio;
    }

    public void setCodigo_dane_municipio(int codigo_dane_municipio) {
        this.codigo_dane_municipio = codigo_dane_municipio;
    }

    public String getDepartamento() {
        return departamento;
    }

    public void setDepartamento(String departamento) {
        this.departamento = departamento;
    }

    public String getDireccion() {
        return direccion;
    }

    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getLatitud() {
        return latitud;
    }

    public void setLatitud(String latitud) {
        this.latitud = latitud;
    }

    public String getLongitud() {
        return longitud;
    }

    public void setLongitud(String longitud) {
        this.longitud = longitud;
    }

    public String getMunicipio() {
        return municipio;
    }

    public void setMunicipio(String municipio) {
        this.municipio = municipio;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getTelefono_celular() {
        return telefono_celular;
    }

    public void setTelefono_celular(String telefono_celular) {
        this.telefono_celular = telefono_celular;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }
}
